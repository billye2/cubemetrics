#!/usr/bin/env node
// Assembles the spine adapter registry from one-file-per-app adapters into a
// generated module — the same seam de-confliction as the catalog generator, so
// parallel build lanes drop an adapter in their own lane and never hand-edit a
// shared array. Each adapter exports `export const adapter: SpineAdapter`.
//
// Run via `npm run build:spine` (also wired into prebuild + pretest).
import { readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const dir = join(root, "src/lib/spine/adapters");
const outFile = join(root, "src/lib/spine/_generated.ts");

const ids = readdirSync(dir)
  .filter((f) => f.endsWith(".ts"))
  .map((f) => f.replace(/\.ts$/, ""))
  .sort();

const bad = ids.filter((id) => !/^[a-z][a-z0-9]*$/.test(id));
if (bad.length) {
  console.error(`build-spine-registry: invalid adapter id(s): ${bad.join(", ")} (lowercase alnum only)`);
  process.exit(1);
}

const imports = ids.map((id) => `import { adapter as ${id} } from "./adapters/${id}";`).join("\n");

const out = `// AUTO-GENERATED by scripts/build-spine-registry.mjs — DO NOT EDIT BY HAND.
// Add an adapter by dropping src/lib/spine/adapters/<id>.ts, then run
// \`npm run build:spine\`. See docs/app-plans/spine-phase1.md.
import type { SpineAdapter } from "./types";
${imports}

export const ADAPTERS: SpineAdapter[] = [${ids.join(", ")}];
`;

writeFileSync(outFile, out);
console.log(`build-spine-registry: wrote ${ids.length} adapters → src/lib/spine/_generated.ts`);
